      $(document).ready(function(){
          $("#cinema").click(function(){
              $("#cinema").addClass("is-active");
              $("#documentaires").removeClass("is-active");
              $("#oeuvres_commentees").removeClass("is-active");
              $("#cinCont").show();
              $("#docCont").hide();
              $("#oeuvCont").hide();
          });        
          $("#documentaires").click(function(){
              $("#documentaires").addClass("is-active");
              $("#cinema").removeClass("is-active");
              $("#oeuvres_commentees").removeClass("is-active");
              $("#docCont").show();
              $("#cinCont").hide();
              $("#oeuvCont").hide();
          });        
          $("#oeuvres_commentees").click(function(){
              $("#oeuvres_commentees").addClass("is-active");
              $("#cinema").removeClass("is-active");
              $("#documentaires").removeClass("is-active");
              $("#oeuvCont").show();
              $("#docCont").hide();
              $("#cinCont").hide();
          });
      }); 